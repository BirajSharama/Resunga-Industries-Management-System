    private void loadEmployees() {
        javax.swing.table.DefaultTableModel model =
                (javax.swing.table.DefaultTableModel) jTable2.getModel();

        model.setRowCount(0);

        for (User u : UserFileDAO.loadUsers()) {
            model.addRow(new Object[]{
                u.getId(),
                u.getEmail(),
                u.getEmail(),
                u.getPassword()
            });
        }
    }

