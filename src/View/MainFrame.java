    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String name = jTextField2.getText();
    String qtyText = jTextField1.getText();
    String supplier = jTextField3.getText();

    if (name.isEmpty() || qtyText.isEmpty() || supplier.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "All fields required");
        return;
    }

    int qty = Integer.parseInt(qtyText);
    AdminController.add(name, qty, supplier, jTable1);

    jTextField2.setText("");
    jTextField1.setText("");
    jTextField3.setText("");
    }//GEN-LAST:event_jButton3ActionPerformed

